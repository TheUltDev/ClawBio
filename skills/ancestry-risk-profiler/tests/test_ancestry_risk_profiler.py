class TestAncestryPosterior:
    """Issue 1 — ancestry over-call: infer_ancestry must emit soft posterior at low confidence."""

    def setup_method(self):
        self.panel = arp.load_aisnp_panel(DATA_DIR / "aisnp_panel.csv")

    def test_infer_ancestry_returns_posterior_key(self):
        """infer_ancestry result must include a 'posterior' probability distribution."""
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        result = arp.infer_ancestry(genotypes, self.panel)
        assert "posterior" in result, "Result must include 'posterior' key"

    def test_posterior_covers_all_superpopulations(self):
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        result = arp.infer_ancestry(genotypes, self.panel)
        for pop in ("AFR", "AMR", "EAS", "EUR", "SAS"):
            assert pop in result["posterior"], f"Missing {pop} in posterior"

    def test_posterior_sums_to_one(self):
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        result = arp.infer_ancestry(genotypes, self.panel)
        total = sum(result["posterior"].values())
        assert abs(total - 1.0) < 0.01, f"Posterior must sum to 1.0, got {total}"

    def test_override_posterior_is_hard_assignment(self):
        """User-supplied ancestry override must set posterior to 1.0 for that population."""
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        result = arp.infer_ancestry(genotypes, self.panel, ancestry_override="EAS")
        assert result["posterior"]["EAS"] == 1.0
        for pop in ("AFR", "AMR", "EUR", "SAS"):
            assert result["posterior"][pop] == 0.0

    def test_low_confidence_report_includes_posterior_table(self, tmp_path):
        """When confidence is low, the markdown report must display the posterior distribution."""
        panel_rsids = list(self.panel.keys())
        # Heterozygous AG at all panel positions creates ambiguous, low-gap likelihoods
        ambiguous_genotypes = {rsid: "AG" for rsid in panel_rsids[:arp.MIN_AISNP_COVERAGE + 5]}
        ancestry_result = arp.infer_ancestry(ambiguous_genotypes, self.panel)

        if ancestry_result["confidence"] != "low":
            pytest.skip("Input did not produce a low-confidence result.")

        associations = arp.load_associations(DATA_DIR / "ancestry_risk_associations.json")
        risks = arp.compute_disease_risks(
            ambiguous_genotypes, ancestry_result["inferred_ancestry"], associations
        )
        arp.generate_report(risks, ancestry_result, tmp_path)
        text = (tmp_path / "ancestry_risk_report.md").read_text()
        assert "posterior" in text.lower() or "probability" in text.lower(), (
            "Low-confidence report must show posterior probability distribution, not a bare hard label"
        )


class TestApol1RecessiveModel:
    """Issue 3 — APOL1 must use a compound-recessive model, not per-allele log-additive."""

    def setup_method(self):
        self.associations = arp.load_associations(DATA_DIR / "ancestry_risk_associations.json")

    def test_single_apol1_allele_is_carrier_not_full_multiplicative_or(self):
        """A single G1 heterozygote must NOT produce the old 1.89x multiplicative OR.
        Recessive model: one allele → carrier note (OR < 1.5); two alleles → high risk."""
        genotypes_carrier = {"rs73885319": "GA", "rs60910145": "CC"}
        risks = arp.compute_disease_risks(genotypes_carrier, "AFR", self.associations)
        ckd = next((r for r in risks if r.disease == "Chronic Kidney Disease"), None)
        if ckd is not None:
            assert ckd.combined_or < 1.5, (
                f"Single APOL1 allele must produce at most a carrier-level OR (<1.5). "
                f"Got {ckd.combined_or}. The per-allele multiplicative model is biologically wrong "
                f"for APOL1 — nephropathy risk is recessive (requires two high-risk alleles)."
            )

    def test_apol1_compound_het_scores_high_or(self):
        """G1+G2 compound het (one allele at each locus = two total risk alleles)
        must produce the validated compound-recessive OR (~7x), not the naive 3.3x product."""
        genotypes_compound = {"rs73885319": "GA", "rs60910145": "CT"}
        risks = arp.compute_disease_risks(genotypes_compound, "AFR", self.associations)
        ckd = next((r for r in risks if r.disease == "Chronic Kidney Disease"), None)
        assert ckd is not None, "CKD must appear for APOL1 compound het in AFR"
        assert ckd.combined_or >= 5.0, (
            f"APOL1 G1+G2 compound het must produce high CKD OR (≥5x from validated literature). "
            f"Got {ckd.combined_or}. The naive multiplicative product of individual per-allele ORs "
            f"(~3.3x) under-estimates the validated compound recessive risk (~7x)."
        )

    def test_apol1_hom_ref_produces_no_ckd_entry(self):
        """A patient carrying zero APOL1 risk alleles must not appear in CKD results."""
        genotypes_ref = {"rs73885319": "GG", "rs60910145": "CC"}
        risks = arp.compute_disease_risks(genotypes_ref, "AFR", self.associations)
        ckd = next((r for r in risks if r.disease == "Chronic Kidney Disease"), None)
        assert ckd is None, "Zero APOL1 risk alleles must produce no CKD entry."


