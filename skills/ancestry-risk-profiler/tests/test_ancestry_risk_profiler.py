class TestProvenanceIntegrity:
    """CI gate: binds every active OR to a PMID that is not known-bad and does
    not exhibit the single-source back-attribution pattern (one paper used as
    the source for multiple unrelated disease categories in the same ancestry).
    """

    def setup_method(self):
        with open(DATA_DIR / "ancestry_risk_associations.json") as f:
            self.data = json.load(f)
        self.assocs = self.data["associations"]
        self.recessive = self.data.get("compound_recessive_groups", {})

    # ------------------------------------------------------------------
    # 1. No null PMIDs in active association entries
    # ------------------------------------------------------------------

    def test_no_null_pmids_in_associations(self):
        null_entries = [
            f"{a['rsid']} / {a.get('disease')} / {a.get('ancestry')}"
            for a in self.assocs
            if a.get("pmid") is None
        ]
        assert null_entries == [], (
            f"Active association entries must not have null PMIDs — use GWAS Catalog "
            f"accession or drop the entry if no paper can be confirmed.\n"
            f"Null PMID entries: {null_entries}"
        )

    def test_no_null_pmids_in_compound_recessive_groups(self):
        null_groups = [
            name for name, g in self.recessive.items()
            if g.get("pmid") is None
        ]
        assert null_groups == [], (
            f"compound_recessive_groups entries must not have null PMIDs.\n"
            f"Affected groups: {null_groups}"
        )

    # ------------------------------------------------------------------
    # 2. No known-bad PMIDs
    # ------------------------------------------------------------------

    def test_no_known_bad_pmids_in_associations(self):
        violations = [
            f"{a['rsid']} / {a.get('disease')} / {a.get('ancestry')} → pmid={a['pmid']}"
            for a in self.assocs
            if str(a.get("pmid", "")) in _KNOWN_BAD_PMIDS
        ]
        assert violations == [], (
            "Known-bad PMIDs detected in association panel — these resolve to wrong papers "
            "(wrong disease, wrong ancestry, or unrelated study).\n"
            f"Violations:\n" + "\n".join(f"  {v}" for v in violations)
        )

    def test_no_known_bad_pmids_in_compound_recessive_groups(self):
        violations = [
            f"{name} → pmid={g['pmid']}"
            for name, g in self.recessive.items()
            if str(g.get("pmid", "")) in _KNOWN_BAD_PMIDS
        ]
        assert violations == [], (
            f"Known-bad PMIDs in compound_recessive_groups: {violations}"
        )

    # ------------------------------------------------------------------
    # 3. No single PMID sourcing associations across >1 disease group
    #
    # A T2D GWAS may cover multiple T2D loci across ancestries (same disease
    # group: "metabolic").  But the same paper cannot legitimately be the
    # primary source for associations in both "metabolic" and "cardiovascular"
    # categories — that is the signature of the blanket back-attribution bug.
    # ------------------------------------------------------------------

    def test_no_pmid_spans_multiple_disease_groups(self):
        from collections import defaultdict
        pmid_to_groups: dict = defaultdict(set)
        pmid_to_examples: dict = defaultdict(list)

        for a in self.assocs:
            pmid = str(a.get("pmid", ""))
            if not pmid or pmid == "None":
                continue
            disease = a.get("disease", "")
            group = _DISEASE_GROUPS.get(disease, disease)
            pmid_to_groups[pmid].add(group)
            pmid_to_examples[pmid].append(
                f"{a['rsid']} {disease} ({a.get('ancestry')})"
            )

        violations = []
        for pmid, groups in pmid_to_groups.items():
            if len(groups) > 1:
                violations.append(
                    f"PMID {pmid} spans disease groups {groups}:\n"
                    + "\n".join(f"    {ex}" for ex in pmid_to_examples[pmid])
                )

        assert violations == [], (
            "Single PMID used as source across multiple unrelated disease categories — "
            "this is the back-attribution pattern that produces fabricated provenance.\n"
            "A T2D GWAS cannot be the source of a CAD or AD odds ratio.\n\n"
            + "\n".join(violations)
        )

    # ------------------------------------------------------------------
    # 3b. No inert baseline_prevalences block (double-counting hazard)
    # ------------------------------------------------------------------

    def test_no_baseline_prevalences_block(self):
        assert "baseline_prevalences" not in self.data, (
            "The inert `baseline_prevalences` block must not be present in "
            "ancestry_risk_associations.json. Absolute-risk scoring was removed in "
            "v1.3.0 to avoid double-counting; leaving the prevalence table in the file "
            "leaves the double-counting bug one wiring change away from returning."
        )

    # ------------------------------------------------------------------
    # 4. All ORs are positive and plausible (catches transcription errors
    #    that would silently produce nonsense AES values)
    # ------------------------------------------------------------------

    def test_all_ors_are_positive(self):
        bad = [
            f"{a['rsid']} / {a.get('disease')} / {a.get('ancestry')} or={a['or']}"
            for a in self.assocs
            if not (0.01 <= a.get("or", 0) <= 100)
        ]
        assert bad == [], (
            f"OR values must be in the range [0.01, 100]. Out-of-range entries:\n"
            + "\n".join(f"  {b}" for b in bad)
        )

    # ------------------------------------------------------------------
    # 5. HFE and APOL1 must not be on the additive path
    # ------------------------------------------------------------------

    def test_hfe_is_recessive_model(self):
        hfe = next((a for a in self.assocs if a["rsid"] == "rs1800562"), None)
        assert hfe is not None, "HFE rs1800562 entry must exist"
