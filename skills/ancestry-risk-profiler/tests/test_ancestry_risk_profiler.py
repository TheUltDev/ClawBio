class TestDataIntegrity:
    """Audit Issue 1 — fabricated citations and direction artifact in LCT entries."""

    def setup_method(self):
        self.associations = arp.load_associations(DATA_DIR / "ancestry_risk_associations.json")

    def test_lct_entries_removed(self):
        """LCT rs4988235 entries must be removed.

        The entries assigned EUR or=0.45 (C allele is minority non-persistence allele,
        coded protective) while non-EUR ORs were 3.2–6.8x (coded as risk). Comparing
        log(OR_ancestry) − log(OR_EUR) across opposite outcome framings manufactures a
        spurious AES of 7–15x. PMID 14507249 (cited as Enattah 2002 LCT) resolves to
        Hashibe 2003 (bladder cancer survival) — a fabricated citation.
        """
        lct_entries = [
            a for a in self.associations["associations"]
            if a.get("disease") == "Lactose Intolerance" or a.get("rsid") == "rs4988235"
        ]
        assert lct_entries == [], (
            f"LCT rs4988235 Lactose Intolerance entries must be removed. "
            f"Found: {[e['rsid'] + '/' + e['ancestry'] for e in lct_entries]}. "
            "These entries mix opposite outcome framings (EUR protective vs. non-EUR risk) "
            "in the AES formula, manufacturing spurious amplification scores."
        )

    def test_no_aes_direction_artifact(self):
        """Every (or_ancestry, eur_or) pair must be on the same side of 1.0.

        An ancestry OR > 1.0 paired with eur_or < 1.0 for the same risk_allele means
        the AES formula is comparing log-ORs for opposite phenotype directions, which
        manufactures a spuriously large AES. Near-null values (within 0.1 of 1.0)
        are exempt because trivial direction ambiguity there is clinically meaningless.
        """
        for assoc in self.associations["associations"]:
            or_anc = assoc["or"]
            eur_or = assoc.get("eur_or", or_anc)
            anc_near_null = abs(or_anc - 1.0) < 0.1
            eur_near_null = abs(eur_or - 1.0) < 0.1
            if anc_near_null or eur_near_null:
                continue
            anc_is_risk = or_anc > 1.0
            eur_is_risk = eur_or > 1.0
            assert anc_is_risk == eur_is_risk, (
                f"Direction artifact in {assoc['rsid']} ({assoc['disease']}, {assoc['ancestry']}): "
                f"or_ancestry={or_anc} vs eur_or={eur_or} are on opposite sides of 1.0. "
                "The AES formula log(OR_anc) − log(OR_eur) compares different phenotype "
                "directions when one is protective and the other is risk."
            )

    def test_aldh2_entry_does_not_use_wrong_pmid(self):
        """ALDH2 rs671 must not reference PMIDs known to be wrong.

        PMID 22561518 = Jin 2012 (vitiligo GWAS), not Wu 2012 (ESCC).
        PMID 20686008 = anaesthesiology case report, not ESCC GWAS.
        """
        aldh2 = next(
            (a for a in self.associations["associations"] if a["rsid"] == "rs671"), None
        )
        assert aldh2 is not None, "ALDH2 rs671 entry must exist in the panel"
        bad_pmids = {"22561518", "20686008"}
        actual_pmid = str(aldh2.get("pmid", "")) if aldh2.get("pmid") is not None else ""
        assert actual_pmid not in bad_pmids, (
            f"ALDH2 rs671 has PMID '{actual_pmid}', which resolves to a wrong paper. "
            "22561518 = Jin 2012 vitiligo GWAS; 20686008 = anaesthesiology case report. "
            "Use the GWAS Catalog accession (GCST001563) or a verified PMID instead."
        )


class TestSoftPosteriorGating:
    """Audit Issue 3 — ancestry confidence must influence disease scoring, not just display."""

    def setup_method(self):
        self.associations = arp.load_associations(DATA_DIR / "ancestry_risk_associations.json")
        self.panel = arp.load_aisnp_panel(DATA_DIR / "aisnp_panel.csv")

    def _make_ancestry_result(self, confidence: str, posteriors: dict) -> dict:
        best = max(posteriors, key=lambda p: posteriors[p])
        return {
            "inferred_ancestry": best,
            "confidence": confidence,
            "posterior": posteriors,
            "aisnp_coverage": 35,
            "overridden": False,
            "scores": {p: 0.0 for p in posteriors},
        }

    def test_abstains_when_top_posterior_below_threshold(self):
        """When confidence is 'low' and top posterior < LOW_POSTERIOR_ABSTAIN,
        _get_risks_with_confidence_gating must return ([], note) rather than running
        disease scoring on a highly ambiguous ancestry assignment."""
        ancestry_result = self._make_ancestry_result(
            "low",
            {"AFR": 0.22, "AMR": 0.21, "EAS": 0.22, "EUR": 0.14, "SAS": 0.21},
        )
        risks, note = arp._get_risks_with_confidence_gating({}, ancestry_result, self.associations)
        assert risks == [], (
            "Must abstain from disease scoring when top posterior < LOW_POSTERIOR_ABSTAIN. "
            f"Got {len(risks)} risk entries instead of 0."
        )
        assert note is not None, "Must return a descriptive note explaining the abstention"
        assert any(kw in note.lower() for kw in ("too low", "not performed", "confidence", "ambiguous")), (
            f"Abstention note must explain why scoring was skipped. Got: {note!r}"
        )

    def test_proceeds_with_note_when_low_confidence_above_threshold(self):
        """When confidence is 'low' but top posterior >= LOW_POSTERIOR_ABSTAIN (0.35),
        _get_risks_with_confidence_gating must run scoring AND return a caveat note."""
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        ancestry_result = arp.infer_ancestry(genotypes, self.panel)

        if ancestry_result["confidence"] != "low":
            pytest.skip("Demo patient did not produce a low-confidence result.")

        top_posterior = max(ancestry_result["posterior"].values())
        if top_posterior < arp.LOW_POSTERIOR_ABSTAIN:
            pytest.skip(f"Demo patient top posterior {top_posterior:.2f} < abstain threshold.")

        risks, note = arp._get_risks_with_confidence_gating(genotypes, ancestry_result, self.associations)
        assert len(risks) > 0, (
            "Should still return disease risks when above the abstain threshold, even with low confidence."
        )
        assert note is not None, (
            "Should return a caveat note for low-confidence scoring (above abstain threshold)."
        )

    def test_no_note_for_high_confidence_ancestry(self):
        """When ancestry confidence is 'high', no scoring note should be emitted."""
        ancestry_result = self._make_ancestry_result(
            "high",
            {"AFR": 0.01, "AMR": 0.01, "EAS": 0.01, "EUR": 0.96, "SAS": 0.01},
        )
        _, note = arp._get_risks_with_confidence_gating({}, ancestry_result, self.associations)
        assert note is None, (
            f"No scoring note should be emitted for high-confidence ancestry assignment. Got: {note!r}"
        )

    def test_no_note_for_user_override(self):
        """When ancestry is user-supplied (overridden=True), no soft-posterior note is emitted."""
        ancestry_result = self._make_ancestry_result(
            "low",
            {"AFR": 0.22, "AMR": 0.21, "EAS": 0.22, "EUR": 0.14, "SAS": 0.21},
        )
        ancestry_result["overridden"] = True  # user explicitly supplied ancestry
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        _, note = arp._get_risks_with_confidence_gating(genotypes, ancestry_result, self.associations)
        assert note is None, (
            "User-supplied ancestry (--ancestry flag) bypasses the soft-posterior gate — no note needed."
        )


