class TestRiskScoring:
    def setup_method(self):
        self.associations = arp.load_associations(
            DATA_DIR / "ancestry_risk_associations.json"
        )

    def test_compute_disease_risks_returns_list(self):
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        risks = arp.compute_disease_risks(genotypes, "SAS", self.associations)
        assert isinstance(risks, list)
        assert len(risks) > 0

    def test_disease_risk_has_required_fields(self):
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        risks = arp.compute_disease_risks(genotypes, "SAS", self.associations)
        r = risks[0]
        for field in ("disease", "combined_or", "aes", "or_eur_combined",
                      "variants_found", "aes_category"):
            assert hasattr(r, field), f"Missing field: {field}"

    def test_disease_risk_has_no_absolute_risk_pct_fields(self):
        """Absolute lifetime risk % was removed — double-counting the baseline is unsound."""
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        risks = arp.compute_disease_risks(genotypes, "SAS", self.associations)
        r = risks[0]
        assert not hasattr(r, "user_lifetime_risk_pct"), "user_lifetime_risk_pct must be removed"
        assert not hasattr(r, "baseline_lifetime_risk_pct"), "baseline_lifetime_risk_pct must be removed"

    def test_disease_risk_has_or_eur_combined(self):
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        risks = arp.compute_disease_risks(genotypes, "SAS", self.associations)
        t2d = next(r for r in risks if r.disease == "Type 2 Diabetes")
        assert isinstance(t2d.or_eur_combined, float)
        assert t2d.or_eur_combined > 0

    def test_t2d_surfaces_for_sas(self):
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        risks = arp.compute_disease_risks(genotypes, "SAS", self.associations)
        diseases = [r.disease for r in risks]
        assert "Type 2 Diabetes" in diseases

    def test_aes_gt1_for_sas_t2d(self):
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        risks = arp.compute_disease_risks(genotypes, "SAS", self.associations)
        t2d = next(r for r in risks if r.disease == "Type 2 Diabetes")
        assert t2d.aes > 1.0, f"Expected AES > 1 for SAS T2D, got {t2d.aes}"

    def test_aes_near_1_for_eur_t2d(self):
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        risks_eur = arp.compute_disease_risks(genotypes, "EUR", self.associations)
        risks_sas = arp.compute_disease_risks(genotypes, "SAS", self.associations)
        t2d_eur = next((r for r in risks_eur if r.disease == "Type 2 Diabetes"), None)
        t2d_sas = next((r for r in risks_sas if r.disease == "Type 2 Diabetes"), None)
        if t2d_eur and t2d_sas:
            assert t2d_sas.aes >= t2d_eur.aes, "SAS AES should be >= EUR AES for T2D"

    def test_diseases_ranked_by_aes(self):
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        risks = arp.compute_disease_risks(genotypes, "SAS", self.associations)
        aes_values = [r.aes for r in risks]
        assert aes_values == sorted(aes_values, reverse=True)

    def test_aes_category_assigned(self):
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        risks = arp.compute_disease_risks(genotypes, "SAS", self.associations)
        valid_cats = {"elevated by ancestry", "neutral", "reduced by ancestry"}
        for r in risks:
            assert r.aes_category in valid_cats

    def test_variants_found_list_nonempty_for_t2d(self):
        genotypes = genotypes_to_simple(parse_genetic_file(DEMO_FILE))
        risks = arp.compute_disease_risks(genotypes, "SAS", self.associations)
        t2d = next(r for r in risks if r.disease == "Type 2 Diabetes")
        assert len(t2d.variants_found) > 0


